            <h3>What is Swachh Bharat Abhiyan</h3>
            <p>Swachh Bharat Abhiyan is a national cleanliness campaign established by the Government of India. This campaign is covering 4041 statutory towns in order to clean roads, streets, and infrastructure of the India. It is a mass movement has run to create a Clean India by 2019. It is a step ahead to the Mahatma Gandhi’s dream of swachh Bharat for healthy and prosperous life. This mission was launched on 2nd of October 2014 (145th birth anniversary of Bapu) by targeting its completeness in 2019 on 150th birth anniversary of Bapu. The mission has been implemented to cover all the rural and urban areas of the India under the Ministry of Urban Development and the Ministry of Drinking Water and Sanitation accordingly</p>
            <p>The first cleanliness drive (on 25th of September 2014) of this mission was started by the Indian Prime Minister, Narendra Modi earlier to its launch. This mission has targeted to solve the sanitation problems as well as better waste management all over the India by creating sanitation facilities to all.  </p>
            <h3>Need of Swachh Bharat Abhiyan</h3>
            <p>Swachh Bharat mission is very necessary to run continuously in India until it gets its goal. It is very essential for the people in India to really get the feeling of physical, mental, social and intellectual well being. It is to make living status advance in India in real means which can be started by bringing all over cleanliness. Below I have mentioned some points proving the urgent need of swachh bharat abhiyan in India: </p>
            <ul>
              <li>It is really very essential to eliminate the open defecation in India as well as making available toilets facility to everyone.</li>
              <li>It is needed in India to convert the insanitary toilets into flushing toilets.</li>
              <li>It is necessary in order to eradicate the manual scavenging system.</li>
              <li>It is to implement the proper waste management through the scientific processes, hygienic disposal, reuse, and recycling of the municipal solid wastes.</li>
              <li>It is to bring behavioral changes among Indian people regarding maintenance of personal hygiene and practice of healthy sanitation methods.</li>
              <li>It is to create global awareness among common public living in rural areas and link it to the public health.</li>
              <li>It is to support working bodies to design, execute and operate the waste disposal systems locally.</li>
              <li>It is to bring private-sector participation to develop sanitary facilities all through the India.</li>
              <li>It is to make India a clean and green India.</li>
              <li>It is necessary to improve the quality of life of people in rural areas.</li>
            </ul>
            <h3>Swachh Bharat Mission in Urban Areas</h3>
            <p>The swachh bharat mission of urban areas aims to cover almost 1.04 crore households in order to provide them 2.6 lakhs of public toilets, 2.5 lakhs of community toilets together with the solid wastes management in every town. Community toilets have been planned to be built in the residential areas where availability of individual household toilets is difficult and public toilets at designated locations including bus stations, tourist places, railway stations, markets, etc. Cleanliness programme in the urban areas (around 4,401 towns) have been planned to be completed over five years till 2019. The costs of programmes are set like Rs 7,366 crore on solid waste management, Rs 1,828 crore on public awareness, Rs 655 crore on community toilets, Rs 4,165 crore on individual household toilets, etc. Programmes which have been targeted to be completed are complete removal of open defecation, converting unsanitary toilets into flush toilets, eradicating manual scavenging, bring behavioral changes among public, and solid waste management.  </p>
            <h3>Gramin Swachh Bharat Mission</h3>
            <p>Gramin swachh bharat mission is a mission implementing cleanliness programmes in the rural areas. Earlier the Nirmal Bharat Abhiyan (also called Total Sanitation campaign, TSC) was established by the Government of India in 1999 to make rural areas clean however now it has been restructured into the Swachh Bharat Mission (Gramin). This campaign is aimed to make rural areas free of open defecation till 2019 for which the cost has been estimated is one lakh thirty four thousand crore rupees for constructing approximately 11 crore 11 lakh toilets in the country. There is a big plan of converting waste into bio-fertilizer and useful energy forms. This mission involves the participation of gram panchayat, panchayat samiti and Zila Parishad. Following are the objectives of Swachh Bharat Mission (Gramin): </p>
            <ul>
              <li>To improve quality of life of people living in the rural areas.
              <li>To motivate local working bodies (such as communities, Panchayati Raj Institutions, etc) to make available the required sustainable sanitation facilities.</li>
              <li>Motivate people to maintain sanitation in rural areas to complete the vision of Swachh Bharat by 2019.</li>
              <li>Develop advance environmental sanitation systems manageable by the community especially to focus on solid and liquid waste management in the rural areas.</li>
              <li>To promote ecologically safe and sustainable sanitation in the rural areas.</li></li>
            </ul>
            <h3>Swachh Bharat-Swachh Vidyalaya Campaign</h3>
            <p>The swachh bharat swachh vidyalaya campaign runs by the Union Ministry of Human Resource Development having same objectives of cleanliness in the schools. A big programme was organized under it from 25th of September 2014 to 31st of October 2014 in the Kendriya Vidyalayas and Navodaya Vidyalya Sangathans where lots of cleanliness activities were held such as discussion over various cleanliness aspects in the school assembly by the students, teachings of Mahatma Gandhi related to cleanliness, cleanliness and hygiene topics, cleaning activities (in the class rooms, libraries, laboratories, kitchen sheds stores, playgrounds, gardens, toilets, pantry areas, etc), cleaning of statue in the school area, speech over the contribution of great people, essay writing competition, debates, art, painting, film, shows, role plays related to hygiene including other many activities on cleanliness and hygiene. It has also been planned to held half an hour cleaning campaign in the schools twice a week involving the cleanliness activities by the teachers, students, parents and community members.</p>
            
